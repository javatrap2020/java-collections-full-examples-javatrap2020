package com.javatrap2020_full_examples_java_collections;

public class Bird {
    private int age;
    private String name;

    public Bird(int age, String name) {
        this.age = age;
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public String getName() {
        return name;
    }
}
